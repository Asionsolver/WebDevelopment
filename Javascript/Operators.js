// Arithmetic Operator
//+, -, *, /, %, ++-->Incremental Operator, -- -->Decremental Operator


var a = 10
var b = 3

console.log(a / b)
console.log(a % b)

//++a-->Pre Incremental
console.log('Pre Incremental')
console.log(a)
console.log(++a)
console.log(++a)

//a++-->Post Incremental
console.log('Post Incremental')
console.log(a)
console.log(a++)
console.log(a++)
console.log(a++)
console.log(a)
console.log(a)

